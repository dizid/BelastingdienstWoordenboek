/**
 * Get a random meeting word
 */
export function getRandomWord() {
  return meetingWords[Math.floor(Math.random() * meetingWords.length)]
}

/**
 * Get definition for a word (with fallback)
 */
export function getDefinition(word) {
  return definitions[word] || "A meeting so rare, even we don't have a definition yet"
}

/**
 * Count words containing a substring
 */
export function countWordsContaining(substring) {
  return meetingWords.filter(w =>
    w.toLowerCase().includes(substring.toLowerCase())
  ).length
}
